package learnJavaAtPrison;

public class ImplementationTricks {
    public static void main(String[] args) {
        int primer=1;
        System.out.println("begining number is "+primer);
        System.out.println("++i "+ ++primer);
        System.out.println("i++ "+ primer++);
        System.out.println("i "+ primer);
        System.out.println("--i "+ --primer);
        System.out.println("i-- "+ primer--);
        System.out.println("i "+ primer);
    }
}
